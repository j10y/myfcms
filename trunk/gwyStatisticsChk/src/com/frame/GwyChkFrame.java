	private void actionButtonActionPerformed(java.awt.event.ActionEvent evt) {
		actionButton.setEnabled(false);
		jComboBox1.setEnabled(false);
		resultMap.clear();

		actionThread = new Thread() {
			@Override
			public void run() {

				if (lastYearPath.getText().equals("")) {
					JOptionPane.showMessageDialog(GwyChkFrame.this, "去年数据文件未选择");
					actionButton.setEnabled(true);
					jComboBox1.setEnabled(true);
					return;
				}

				if (thisYearPath.getText().equals("")) {
					JOptionPane.showMessageDialog(GwyChkFrame.this, "今年数据文件未选择");
					actionButton.setEnabled(true);
					jComboBox1.setEnabled(true);
					return;
				}

				HashSet<String> operator = new HashSet<String>();

				operator.add("+");
				operator.add("-");
				operator.add("*");
				operator.add("/");

				operator.add("==");
				operator.add(">");
				operator.add(">=");
				operator.add("<");
				operator.add("<=");
				operator.add("<>");

				operator.add("(");
				operator.add(")");

				operator.add("!");
				operator.add("%");
				operator.add("&&");
				operator.add("||");
				try {

					// InputStream in =
					// GwyChkFrame.class.getResourceAsStream("/source" +
					// jComboBox1.getSelectedIndex());
					// InputStreamReader inReader = new InputStreamReader(in,
					// "GBK");
					// BufferedReader br = new BufferedReader(inReader);

					FileReader reader = new FileReader("source" + jComboBox1.getSelectedIndex());
					BufferedReader br = new BufferedReader(reader);

					SecretKey keys = DesEncrypter.open();
					DesEncrypter encrypter = new DesEncrypter(keys);

					InputStream is1 = new FileInputStream(lastYearPath.getText());
					InputStream is2 = new FileInputStream(thisYearPath.getText());
					Workbook wb1 = Workbook.getWorkbook(is1);
					Workbook wb2 = Workbook.getWorkbook(is2);

					String str = null;
					progressBar.setValue(20);
					while ((str = br.readLine()) != null) {
						str = encrypter.decrypt(str);
						str = str.replaceAll(" ", "");
						if (!str.isEmpty() && !str.startsWith("#")) {
							System.out.println(str);

							String str1[] = str.split("\\?");

							String expression = str1[0];
							String reason = str1[1];

							String experssions[] = expression.split(";");

							for (int i = 0; i < experssions.length; i++) {
								if (!operator.contains(experssions[i])
										&& Pattern.matches("\\d{1,3},\\d{1,3},\\w{1,3},\\d{1,3}", experssions[i])) {

									String values[] = experssions[i].split(",");

									String value = "";

									if (values[0].equals("1")) {
										Sheet s1 = wb1.getSheet(Integer.parseInt(values[1]));

										value = s1
												.getCell(Common.getLetterNumber(values[2]) - 1, Integer.parseInt(values[3]) - 1)
												.getContents();
									}
									if (values[0].equals("2")) {
										Sheet s2 = wb2.getSheet(Integer.parseInt(values[1]));
										value = s2.getCell(Common.getLetterNumber(values[2]) - 1, Integer.parseInt(values[3]) - 1)
												.getContents();
									}

									if (value.equals("")) {
										value = "0";
									}
									experssions[i] = value;
								}
							}

							StringBuffer sb = new StringBuffer();

							for (int j = 0; j < experssions.length; j++) {
								sb.append(experssions[j]);
							}

							ExpressionExecutor ee = new ExpressionExecutor();

							List<ExpressionToken> tokenList = ee.analyze(sb.toString());

							System.out.println(tokenList);
							tokenList = ee.compile(tokenList);

							if (
//							 true ||
							!Boolean.valueOf(ee.execute(tokenList).toJavaObject().toString())) {
								// System.out.println("错误;" + reason);
								resultMap.put(reason, sb.toString());
							}

						}
					}

					progressBar.setValue(80);

					br.close();
					reader.close();

					DefaultTableModel tableModel = (DefaultTableModel) resultTable.getModel();
					 tableModel.setRowCount(0);// 清除原有行

					resultDialog.setTitle(jComboBox1.getSelectedItem().toString() + "校核结果");

					int i = 0;
					for (String key : resultMap.keySet()) {
						Object[] arr = new Object[3];
						arr[0] = String.valueOf(i + 1);
						arr[1] = key;
						arr[2] = resultMap.get(key);
						tableModel.addRow(arr);
						i++;
					}
					resultTable.invalidate();

					resultDialog.setVisible(true);
					resultDialog.setLocationRelativeTo(resultDialog.getOwner());

				} catch (FileNotFoundException e) {
					JOptionPane.showMessageDialog(GwyChkFrame.this, "发生异常！ 抛出的异常:" + e);
					e.printStackTrace();
				} catch (IOException e) {
					JOptionPane.showMessageDialog(GwyChkFrame.this, "发生异常！ 抛出的异常:" + e);
					e.printStackTrace();
				} catch (BiffException e) {
					JOptionPane.showMessageDialog(GwyChkFrame.this, "发生异常！ 抛出的异常:" + e);
					e.printStackTrace();
				} catch (IllegalExpressionException e) {
					JOptionPane.showMessageDialog(GwyChkFrame.this, "发生异常！ 抛出的异常:" + e);
					e.printStackTrace();
				} catch (ParseException e) {
					JOptionPane.showMessageDialog(GwyChkFrame.this, "发生异常！ 抛出的异常:" + e);
					e.printStackTrace();
				}

				progressBar.setValue(0);
				actionButton.setEnabled(true);
				jComboBox1.setEnabled(true);

			}

		};
		actionThread.start();
	}

	private void exportButtonActionPerformed(java.awt.event.ActionEvent evt) {

		FileSystemView fsv = FileSystemView.getFileSystemView();
		JFileChooser fc = new JFileChooser(fsv.getHomeDirectory());
		fc.setDialogType(JFileChooser.SAVE_DIALOG);
		fc.setFileSelectionMode(JFileChooser.FILES_ONLY);

		fc.setFileFilter(new javax.swing.filechooser.FileFilter() {

			@Override
			public boolean accept(File f) {
				return f.isDirectory() || f.getName().endsWith(".xls");
			}

			@Override
			public String getDescription() {
				return "xls文件";
			}

		});

		int i = fc.showDialog(resultDialog, "保存");
		if (i == JFileChooser.APPROVE_OPTION) {

			File file = fc.getSelectedFile();
			String URL = file.getPath();
			if (!URL.endsWith(".xls")) {
				URL = URL + ".xls";
			}

			System.out.println(URL);

			FileOutputStream fos = null;
			WritableWorkbook wwb = null;

			try {
				fos = new FileOutputStream(URL);

				wwb = Workbook.createWorkbook(fos);
				WritableSheet ws = wwb.createSheet(jComboBox1.getSelectedItem().toString(), 0);
				ws.setColumnView(0, 54);
				ws.setColumnView(1, 34);

				ws.mergeCells(0, 0, 1, 0);

				WritableFont wf = new WritableFont(WritableFont.createFont("黑体"), 16, WritableFont.BOLD);
				WritableCellFormat wcf = new WritableCellFormat(wf);
				wcf.setAlignment(jxl.format.Alignment.CENTRE);
				wcf.setVerticalAlignment(jxl.format.VerticalAlignment.CENTRE);
				Label head1 = new Label(0, 0, jComboBox1.getSelectedItem().toString() + "校核报告");
				head1.setCellFormat(wcf);
				ws.addCell(head1);

				Label head2 = new Label(0, 1, "填报单位：");
				ws.addCell(head2);

				Label head3 = new Label(1, 1, "校核时间：" + new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()));
				ws.addCell(head3);

				WritableFont font = new WritableFont(WritableFont.createFont("宋体"), 10, WritableFont.NO_BOLD);
				WritableCellFormat cfResult = new WritableCellFormat(font);
				cfResult.setBorder(Border.ALL, jxl.format.BorderLineStyle.THIN);
				// cfResult.setWrap(true);

				Label label1 = new Label(0, 2, "校核结果");
				label1.setCellFormat(cfResult);
				ws.addCell(label1);

				Label label2 = new Label(1, 2, "建议值");
				label2.setCellFormat(cfResult);
				ws.addCell(label2);

				int j = 3;
				for (String key : resultMap.keySet()) {

					label1 = new Label(0, j, key);
					label1.setCellFormat(cfResult);
					ws.addCell(label1);

					label2 = new Label(1, j, resultMap.get(key));
					label2.setCellFormat(cfResult);
					ws.addCell(label2);
					j++;
				}

				wwb.write();

			} catch (FileNotFoundException e) {
				JOptionPane.showMessageDialog(this, "发生异常！ 抛出的异常:" + e);
				e.printStackTrace();
			} catch (IOException e) {
				JOptionPane.showMessageDialog(this, "发生异常！ 抛出的异常:" + e);
				e.printStackTrace();
			} catch (RowsExceededException e) {
				JOptionPane.showMessageDialog(this, "发生异常！ 抛出的异常:" + e);
				e.printStackTrace();
			} catch (WriteException e) {
				JOptionPane.showMessageDialog(this, "发生异常！ 抛出的异常:" + e);
				e.printStackTrace();
			} finally {
				if (wwb != null) {
					try {
						wwb.close();
					} catch (WriteException e) {
						e.printStackTrace();
					} catch (IOException e) {
						e.printStackTrace();
					}
				}

				if (fos != null) {
					try {
						fos.close();
					} catch (IOException e) {
						e.printStackTrace();
					}
				}
			}
		}
	}

